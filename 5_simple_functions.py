def add(x,y):
    return x+y

print(add(5,4)) #Output - 9

def multiply(x,y):
    return x*y

print(multiply(5,4)) #Output - 20

def subtract(x,y):
    return x-y  

print(subtract(5,4)) #Output - 1

def divide(x,y):
    return x//y 

print(divide(20,5)) #Output - 4

def remainder(x,y):
    return x%y

print(remainder(20,3)) #Output - 2
